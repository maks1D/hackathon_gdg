/**
 * Lookup inventive principles for a given improving/worsening parameter pair.
 * Returns an empty array if the intersection is undefined in the matrix.
 */
export function lookupMatrix(improvingId: number, worseningId: number): number[] {
  if (improvingId === worseningId) return [];
  const key = `${improvingId}-${worseningId}`;
  return CONTRADICTION_MATRIX[key] ?? [];
}
